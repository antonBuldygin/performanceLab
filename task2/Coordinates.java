package org.example.task2;

public class Coordinates {
    String x;
    String y;

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }

    public Coordinates() {
    }

    public Coordinates(String x, String y) {
        this.x = x;
        this.y = y;
    }


}
